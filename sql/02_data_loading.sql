-- =========================================
-- Pizza Sales Analysis
-- Data Loading Instructions
-- =========================================

-- This project uses CSV files as the data source.
-- The CSV files are located in the /data directory.

-- Data can be loaded into MySQL using LOAD DATA INFILE
-- or any MySQL client / GUI tool.

-- Example: Load orders data
-- LOAD DATA INFILE 'path/to/orders.csv'
-- INTO TABLE orders
-- FIELDS TERMINATED BY ','
-- ENCLOSED BY '"'
-- LINES TERMINATED BY '\n'
-- IGNORE 1 ROWS;

-- Example: Load order_details data
-- LOAD DATA INFILE 'path/to/order_details.csv'
-- INTO TABLE order_details
-- FIELDS TERMINATED BY ','
-- ENCLOSED BY '"'
-- LINES TERMINATED BY '\n'
-- IGNORE 1 ROWS;

